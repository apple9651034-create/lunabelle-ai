/**
 * 일간의 기본 성향 요약 생성
 */
export function generateDayMasterSummary(dayMaster: string): string {
  const characteristic = HEAVENLY_STEM_CHARACTERISTICS[dayMaster];
  
  if (!characteristic) {
    return '';
  }
  
  return `
【 ${characteristic.stem} 일간의 기본 성향 】

${characteristic.personality}

✨ 강점: ${characteristic.strength}

⚠️ 약점: ${characteristic.weakness}

🌟 운세 경향: ${characteristic.fortune_tendency}
`;
}

/**
 * 모든 천간의 키워드 조회
 */
export function getStemKeywords(dayMaster: string): string[] {
  return HEAVENLY_STEM_CHARACTERISTICS[dayMaster]?.keywords || [];
}
